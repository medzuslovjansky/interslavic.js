import {Multirator} from "../utils/multirate";

export const russian = new Multirator()
    .add(/zemj/, ['zemľ'])
    .add(/råz/, ['raz','ras'])
    .add(/prě/, ['pre','pere'])
    .add(/ti sę$/, ['ться'])
    .add(/(.)(.)ě(?!$)/, ['$1$2e','$1e$2e'])
    .add(/(.)(.)å/, ['$1$2a','$1o$2o'])
    .add(/a/, ['а'])
    .add(/å/, ['а','о'])
    .add(/b/, ['б'])
    .add(/c/, ['ц'])
    .add(/č/, ['ч'])
    .add(/ć/, ['ч','ц','щ'])
    .add(/d/, ['д'])
    .add(/ď/, ['д','дь'])
    .add(/đ/, ['дж','жд','д','ж'])
    .add(/e/, ['е'])
    .add(/ě/, ['е','и'])
    .add(/ę/, ['я','е','а'])
    .add(/ė/, ['е'])
    .add(/f/, ['ф'])
    .add(/g/, ['г'])
    .add(/h/, ['х'])
    .add(/i/, ['и'])
    .add(/k/, ['к'])
    .add(/l/, ['л'])
    .add(/ľ/, ['л','ль'])
    .add(/m/, ['м'])
    .add(/n/, ['н'])
    .add(/ń/, ['н','нь'])
    .add(/o/, ['о'])
    .add(/p/, ['п'])
    .add(/r/, ['р'])
    .add(/ŕ/, ['р','рь'])
    .add(/s/, ['с'])
    .add(/š/, ['ш'])
    .add(/ś/, ['с','сь'])
    .add(/t/, ['т'])
    .add(/ť/, ['ть'])
    .add(/u/, ['у'])
    .add(/v/, ['в'])
    .add(/y/, ['ы'])
    .add(/z/, ['з'])
    .add(/ų/, ['о','у'])
    .add(/ź/, ['з','зь'])
    .add(/ž/, ['ж'])
    .add(/ȯ/, ['о','е'])
    .add(/([аеийоуыэюяъь])j/, ['$1й'])
    .add(/j/, ['й','ь'])
    .add(/а /, ['а ', 'ая '])
    .add(/ке /, ['кие '])
    .add(/ьа/, ['я'])
    .add(/^йа/, ['я'])
    .add(/^йа/, ['я'])
    .add(/^йе/, ['е','и'])
    .add(/^йи/, ['и','е'])
    .add(/^йо/, ['о','е'])
    .add(/^йу/, ['у'])
    .add(/йа/, ['я','ья','ъя'])
    .add(/йе/, ['е','ье','ъе'])
    .add(/йи/, ['и','ьи','ъи'])
    .add(/йо/, ['е','ье','ъе'])
    .add(/йу/, ['ю','ью','ъю'])
    .add(/йя/, ['я'])
    .add(/йю/, ['ю'])
    .add(/ч$/, ['чь'])
    .add(/ч /, ['чь '])
    .add(/ти$/, ['ти','ть'])
    .add(/ти /, ['ти ','ть '])
    .add(/ы$/, ['ы', 'ий','ый'])
    .add(/ы /, ['ы ', 'ий ','ый '])
    .add(/кть$/, ['кть','чь'])
    .add(/ье$/, ['ье','ие'])
    .add(/ьи$/, ['ьи','ий'])
    .add(/ьи /, ['ьи','ий'])
    .add(/([аеийоуыэюяъь])[ъь]/, ['$1'])
    .add(/([^аеиоуыэюя])[й]/, ['$1ь'])
    .add(/иа$/, ['ия'])
    .add(/чск/, ['ческ'])
;
